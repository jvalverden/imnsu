    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:        
        if (!this.editar) {
            this.in.persona = new PersonaVo();
        }
        this.in.persona.setNombre(jtfNombre.getText());
        this.in.persona.setPaterno(jtfPaterno.getText());
        this.in.persona.setMaterno(jtfMaterno.getText());
        this.in.persona.setIdtipoestado(((tipoEstadoVo) jcbTipoEstado.getSelectedItem()).getId());
        
        this.in.persona.setSexo((jcbSexo.getSelectedItem().toString().compareTo("Masculino") == 0) ? "M" : "F");
        this.in.persona.setEdad(Integer.parseInt(jftfEdad.getText()));

        this.in.persona.setInterior(jchOrigen.isSelected());
        this.in.persona.setProcedencia(jtfOrigen.getText());
        this.in.persona.setTelefono(jtfTelefono.getText());
        this.in.persona.setTelefonoresponsable(jtffonoreponsalble.getText());
        this.in.persona.setMedicoremitente(jtfMedico.getText());
        this.in.persona.setInstituicionprocedente(jtfInstitucion.getText());
        this.in.persona.setEnespera(jchEnespera.isSelected());
        this.in.persona.setFechainicioespera(new Timestamp(jxdpFechaEspera.getDate().getTime()));
        this.in.persona.setFechatentativa(new Timestamp(jxdpFechaTentativa.getDate().getTime()));
        this.in.persona.setIdtipoestudiosolicitado(((tipoEstudioVo)jcbTipoEstudio.getSelectedItem()).getId());
        if (this.editar) {
            personaDao.updatePersona(in.persona);
        } else {
            personaDao.insertPersona(in.persona);
        }
        JOptionPane.showMessageDialog(new JFrame(), "Registro Exitoso!!!");
        dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

