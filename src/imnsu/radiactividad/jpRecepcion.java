    public static double fijarNumero(double numero, int digitos) {
        double resultado;
        //aqui seteamos para todos
        digitos = 3;
        resultado = numero * Math.pow(10, digitos);
        resultado = Math.round(resultado);
        resultado = resultado / Math.pow(10, digitos);
        return resultado;
    }

